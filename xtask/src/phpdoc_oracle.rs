/// Run `php dump.php`, feeding `input` (C-escaped lines) on stdin and returning
/// its stdout verdict lines.
fn run_oracle(harness: &Path, dump: &Path, input: &[u8]) -> Result<Vec<u8>, String> {
    let mut child = Command::new("php")
        .arg(dump)
        .current_dir(harness)
        .stdin(Stdio::piped())
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .spawn()
        .map_err(|e| format!("failed to spawn php: {e}"))?;

    {
        let mut stdin = child.stdin.take().ok_or("no stdin on php child")?;
        stdin
            .write_all(input)
            .map_err(|e| format!("writing to php stdin: {e}"))?;
    }

    let out = child
        .wait_with_output()
        .map_err(|e| format!("waiting on php: {e}"))?;
    if !out.status.success() {
        return Err(format!(
            "dump.php exited with {}: {}",
            out.status,
            String::from_utf8_lossy(&out.stderr)
        ));
    }
    Ok(out.stdout)
}

/// Read the installed phpstan/phpdoc-parser version from the harness lock file.
fn read_phpdoc_version(harness: &Path) -> Option<String> {
    let lock = std::fs::read_to_string(harness.join("composer.lock")).ok()?;
    let idx = lock.find("\"phpstan/phpdoc-parser\"")?;
    let after = &lock[idx..];
    let vpos = after.find("\"version\"")?;
    let vslice = &after[vpos..];
    let start = vslice.find(':')?;
    let q1 = vslice[start..].find('"')? + start + 1;
    let q2 = vslice[q1..].find('"')? + q1;
    Some(vslice[q1..q2].to_owned())
}

/// A minimal `which`: is `cmd` runnable? (`--version` returns success on php).
fn which(cmd: &str) -> Option<PathBuf> {
    let path = std::env::var_os("PATH")?;
    for dir in std::env::split_paths(&path) {
        let candidate = dir.join(cmd);
        if candidate.is_file() {
            return Some(candidate);
        }
    }
    None
}
